package model;

/**
 *
 * @author flavi
 */
public class Pizza extends Item {
    private String sabor;

    public Pizza() {
    }

    public String getSabor() {
        return sabor;
    }

    public void setSabor(String sabor) {
        this.sabor = sabor;
    }

    @Override
    public String toString() {
        return "Pizza{" + "sabor=" + sabor + '}';
    }
    
}
